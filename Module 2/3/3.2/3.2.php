<?php
// Создаем пустой массив для хранения нечетных чисел
$odd_numbers = array();

// Используем цикл for для заполнения массива
for ($i = 1; $i <= 99; $i += 2) {
    $odd_numbers[] = $i;
}

// Выводим содержимое массива на экран
echo "Массив нечетных чисел от 1 до 99:\n";
print_r($odd_numbers);
?>
